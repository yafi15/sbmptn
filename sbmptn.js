window.onload = function () {
    var kodeptn = document.getElementById("kodeptn"),
        jurusan = document.getElementById("jurusan"),
        prodi = document.getElementById("prodi");
    for (var vkodeptn in dataPTN) {
        kodeptn.options[kodeptn.options.length] = new Option(vkodeptn, vkodeptn);
    }
    kodeptn.onchange = function () {
        jurusan.length = 1; // remove all options bar first
        prodi.length = 1; // remove all options bar first
        if (this.selectedIndex < 1) return; // done   
        for (var vjurusan in dataPTN[this.value]) {
            jurusan.options[jurusan.options.length] = new Option(vjurusan, vjurusan);
        }
    }
    kodeptn.onchange(); // reset in case page is reloaded
    jurusan.onchange = function () {
        prodi.length = 1; // remove all options bar first
        if (this.selectedIndex < 1) return; // done   
        var vprodi = dataPTN[kodeptn.value][this.value];
        for (var i = 0; i < vprodi.length; i++) {
            prodi.options[prodi.options.length] = new Option(vprodi[i], vprodi[i]);
        }
    }
}